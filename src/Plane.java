public class Plane
{
    private double planeX;
    private double planeY;
    
    public Plane()
    {
        this.planeX = 0;
        this.planeY = 0.66;
    }
    
    public double getPlaneX()
    {
        return this.planeX;
    }
    
    public double getPlaneY()
    {
        return this.planeY;
    }
    
    public void setPlaneX(double planeX)
    {
        this.planeX = planeX;
    }
    
    public void setPlaneY(double planeY)
    {
        this.planeY = planeY;
    }
}
